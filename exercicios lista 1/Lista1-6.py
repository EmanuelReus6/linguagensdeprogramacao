'''Faça um programa que leia quanto dinheiro uma pessoa tem na carteira e mostre quantos Dólares ela pode
comprar.
Considere: US$ 1.00 = R$ 5.41'''

reais = float(input("Digite um valor em reais"))

print(f"O valor digitado em reais (R${reais}) é equivalente a ${reais/5.41:.2} dolares")








